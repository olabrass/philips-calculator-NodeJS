
// The function for adding two numbers
function addNum(firstNum, secondNum){
    return firstNum + secondNum;
}
// The function for subtracting one number from the other
function subtractNum(firstNum, secondNum){
    return firstNum - secondNum;
}
// The function for Multiplying two numbers
function multiplyNum(firstNum, secondNum){
    return firstNum * secondNum;
}
// The function for dividing a number with another number
function divideNum(firstNum, secondNum){
    return firstNum / secondNum;
}


// I export the modules here
export{addNum, subtractNum, multiplyNum, divideNum};